export function markdownCheckList(items: ReadinessCheck[], emptyMessage: string): string[] {
  if (items.length === 0) {
    return [`- ${emptyMessage}`];
  }
  return items.map((item) => `- [${item.severity}] ${item.id}: ${item.detail}`);
}

export function markdownIssueList(items: string[], emptyMessage: string): string[] {
  if (items.length === 0) {
    return [`- ${emptyMessage}`];
  }
  return items.map((item) => `- ${item}`);
}

export function markdownNumberedList(items: string[], emptyMessage: string): string[] {
  if (items.length === 0) {
    return [`1. ${emptyMessage}`];
  }
  return items.map((item, index) => `${index + 1}. ${item}`);
}
